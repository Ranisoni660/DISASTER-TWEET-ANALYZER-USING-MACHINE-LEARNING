    sentiment = 'Positive' if sentiment_scores['compound'] >= 0.05 else 'Negative' if sentiment_scores['compound'] <= -0.05 else 'Neutral'

    return {
        'tweet': tweet,
        'is_disaster': prediction[0],
        'location': locations[0] if locations else 'Unknown',
        'category': category,
        'sentiment': sentiment
    }

